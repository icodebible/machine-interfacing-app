#!/usr/bin/env node
/**
 * Synchronizes local offline SVG icons.
 *
 * It uses the scan output from scripts/scan-offline-svg-icons.mjs,
 * then ensures each detected icon has a local SVG under:
 *   src/assets/icons/material/<icon>.svg
 *
 * If @material-design-icons/svg is installed, it uses real Material SVGs.
 * Otherwise, it keeps the bundled fallback SVGs already included in this patch.
 *
 * It always regenerates:
 *   src/app/core/icons/offline-icons.generated.ts
 */
import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const iconDir = path.join(root, 'src/assets/icons/material');
const generatedPath = path.join(root, 'src/app/core/icons/offline-icons.generated.ts');

function exists(file) {
  return fs.existsSync(file);
}

function sanitizeSvg(svg) {
  return svg
    .replace(/<\?xml[\s\S]*?\?>/g, '')
    .replace(/<!DOCTYPE[\s\S]*?>/gi, '')
    .replace(/\s(width|height)="[^"]*"/g, '')
    .replace(/fill="(?!none)[^"]*"/g, 'fill="currentColor"')
    .replace(/<svg\b([^>]*)>/, '<svg$1>')
    .trim();
}

function fallbackSvg() {
  return '<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24"><rect x="4" y="4" width="7" height="7" rx="1.5" fill="currentColor"/><rect x="13" y="4" width="7" height="7" rx="1.5" fill="currentColor"/><rect x="4" y="13" width="7" height="7" rx="1.5" fill="currentColor"/><rect x="13" y="13" width="7" height="7" rx="1.5" fill="currentColor"/></svg>';
}

function findMaterialSvg(icon) {
  const packageRoot = path.join(root, 'node_modules/@material-design-icons/svg');
  const candidates = [
    path.join(packageRoot, 'filled', `${icon}.svg`),
    path.join(packageRoot, 'outlined', `${icon}.svg`),
    path.join(packageRoot, 'round', `${icon}.svg`),
    path.join(packageRoot, 'sharp', `${icon}.svg`),
    path.join(packageRoot, 'two-tone', `${icon}.svg`),
  ];

  for (const candidate of candidates) {
    if (exists(candidate)) return candidate;
  }

  return null;
}

function readScanIcons() {
  const scanPath = path.join(root, 'src/assets/icons/offline-icon-scan.json');
  if (!exists(scanPath)) {
    throw new Error('offline-icon-scan.json not found. Run scripts/scan-offline-svg-icons.mjs first.');
  }

  const scan = JSON.parse(fs.readFileSync(scanPath, 'utf8'));
  return (scan.icons || []).map((item) => item.name).sort();
}

fs.mkdirSync(iconDir, { recursive: true });
fs.mkdirSync(path.dirname(generatedPath), { recursive: true });

const icons = readScanIcons();

for (const icon of icons) {
  const local = path.join(iconDir, `${icon}.svg`);
  const materialSvg = findMaterialSvg(icon);

  if (materialSvg) {
    const svg = sanitizeSvg(fs.readFileSync(materialSvg, 'utf8'));
    fs.writeFileSync(local, `${svg}\n`, 'utf8');
  } else if (!exists(local)) {
    fs.writeFileSync(local, `${fallbackSvg()}\n`, 'utf8');
  }
}

const items = icons.map((name) => {
  const file = path.join(iconDir, `${name}.svg`);
  const svg = sanitizeSvg(fs.readFileSync(file, 'utf8'));
  return { name, svg };
});

const generated = `// AUTO-GENERATED by scripts/sync-offline-svg-icons.mjs.
// Do not edit this file manually. Add SVGs under src/assets/icons/material/
// and run: npm run icons:sync

export interface OfflineMaterialIcon {
  name: string;
  svg: string;
}

export const OFFLINE_MATERIAL_ICONS: OfflineMaterialIcon[] = ${JSON.stringify(items, null, 2)};
`;

fs.writeFileSync(generatedPath, generated, 'utf8');

console.log(`Synchronized ${icons.length} offline SVG icon(s).`);
console.log(`Generated ${path.relative(root, generatedPath)}`);
